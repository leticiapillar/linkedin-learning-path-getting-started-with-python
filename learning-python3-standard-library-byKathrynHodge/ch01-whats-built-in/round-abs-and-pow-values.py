# Python Rounding, Absolute Value, and Exponents

# round()
print("round 1.5 value:", round(1.5))
print("round 1.6 value:", round(1.6))
print("round 1.4 value:", round(1.4))

print("round 1.573 value:", round(1.573, 2))
print("round 1.626 value:", round(1.626, 2))
print("round 1.454 value:", round(1.454, 2))

# abs()
print("abs value of -14  :", abs(-14))
print("abs value of -3.14:", abs(-3.14))

# pow()
print("2² = 4:", pow(2,2))
print("3² = 9:", pow(3,2))
