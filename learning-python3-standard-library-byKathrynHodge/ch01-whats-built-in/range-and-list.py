# 04 Range -> range instance that holds all nums counting by one between 0 and first input
# List -> lists numbers from the inputted tuple

rangeNumbers = range(30)
print("range numbers 0 to 29:", list(rangeNumbers))

rangeNumbers = range(10, 50)
print("range numbers 10 to 49:", list(rangeNumbers))

rangeNumbers = range(5, 50, 5)
print("range numbers 5 to 49, jump 5:", list(rangeNumbers))


