# 03 Calculating Length

# len() --> returns length
firstName = "Leticia"
print(f"My first name {firstName} has {len(firstName)} characters.")

lastName = "Lisboa"
print(f"My last name {lastName} has {len(lastName)} characters.")

nums = [2, 4, 6, 7, 8, 10]
print(f"The nums list {nums}, has {len(nums)} elements")

foods = ["rice", "beans", "apple", "chicken"]
print(f"The foods list {foods}, has {len(foods)} elements")

supermarket = {"apple": 5, "orange": 6, "bread": 1, "milk": 1, "coffee": 1}
print(f"The supermarket dictionary {supermarket}, has {len(supermarket)} items")
