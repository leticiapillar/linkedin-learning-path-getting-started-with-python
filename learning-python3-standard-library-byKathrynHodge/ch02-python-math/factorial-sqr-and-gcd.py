# 02 Math Module Part 2
import math


# Factorial & Square Root
print("Factorial & Square Root")
print("Factorial 3!  :", math.factorial(3))
print("Square root 64:", math.sqrt(64))


# Greatest Common Denominator GCD
print("Greatest Common Denominator GCD")
print("GCD of 8/52:", math.gcd(8, 52))
print("GCD of 52/8:", math.gcd(52, 8))

print("8/52:", 8/52)
print("2/13:", 2/13)


# Degrees and Radians
print("Degrees and Radians")
print("radians of 360   :", math.radians(360))
print("Pi * 2           :", math.pi * 2)
print("degrees of Pi * 2:", math.degrees(math.pi * 2))
