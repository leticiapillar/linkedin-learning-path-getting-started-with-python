# 01 Math Module Part I
import math

# Constants
print("pi  :", math.pi)
print("e   :", math.e)
print("nan :", math.nan)
print("inf :", math.inf)
print("-inf:", -math.inf)


# Trigonometry
print("Trigonometry cos:", math.cos(math.pi / 4))
print("Trigonometry sin:", math.sin(math.pi / 4))


# Ceiling and Floor
num1 = 47.9
print("num 1:", num1)
print("ceil :", math.ceil(num1))
print("floor:", math.floor(num1))
