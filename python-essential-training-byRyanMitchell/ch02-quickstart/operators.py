# 03 Operators

# Arithmetic Operators
print("addition      :", 1 + 1)
print("subtraction   :", 2 - 1)
print("multiplication:", 2 * 2)
print("division      :", 10 / 2)
print("division      :", 10 / 3)

print("exponential   :", 5 ** 2)
print("module        :", 20 % 6)

# Arithmetic Operators with Strings
print("+ operator: ", "string 1" + "string 2")
print("* operator: ", "- string 1 -" * 4)

# Comparison, Logical, and Membership Operators

# Comparison Operators
print("True == True?", True == True)
print("4 < 5?", 4 < 5)
print("5 <= 5?", 5 <= 5)
print("5 > 2?", 5 > 2)
print("5 >= 2?", 5 >= 2)

# Logical Operators (and, or, not)
print("True and True:", True and True)
print("True and False:", True and False)

print("True or False:", True or False)
print("False or False:", False or False)

print("not True:", not True)
print("not False:", not False)

# Membership Operators
print("1 in [1,2,3]:", 1 in [1,2,3])

print("10 in [1,2,3]:", 10 in [1,2,3])
print("10 not in [1,2,3]:", 10 not in [1,2,3])

print("'cat' in 'my pet cat':", "cat" in "my pet cat")
print("'cat' not in 'my pet cat':", "cat" not in "my pet cat")
print("'dog' in 'my pet cat':", "dog" in "my pet cat")

