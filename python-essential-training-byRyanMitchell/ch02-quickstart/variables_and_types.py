# 01 Basic Data Types

# Defining a variable
x = 5
print(x)

name = "Leticia"
print(name)

print(type(name))
print(type(x))

# Numbers
print(type(1.5))

f = 1.23456789
print(f)

f = 12345678.9
print(f)

print(type(2j))
print(1j * 1j)

# Strings
