# 01 Ints and Floats

print("Some examples: operations result in float type")
print("20 / 4  :", 20/4)
print("4 + 4.0 :", 4+4.0)
print("4 * 4.0 :", 4*4.0)
print("4 ** 4.0:", 4**4.0)


print("implicit casting")
print("int(4 ** 4.0) :", int(4 ** 4.0))
print("int(8.9)      :", int(8.9))
print("int(8.9999999):", int(8.9999999))

print("round")
print("1.0 - 1.2          :", 1.0 - 1.2)
print("round(1.0 - 1.2)   :", round(1.0 - 1.2))
print("round(1.0 - 1.2, 2)   :", round(1.0 - 1.2,2 ))

print("14 / 3          :", 14 / 3)
print("round(14 / 3)   :", round(14 / 3))
print("round(14 / 3, 2):", round(14 / 3, 2))
